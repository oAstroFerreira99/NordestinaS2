-----------------------------------------------------------------------------------------------------------------------------------------
-- IDENTIDADE
-----------------------------------------------------------------------------------------------------------------------------------------
function vRP.IdentityArcadius(Passport)
    local personagem = vRP.Query("characters/Person",{id = Passport})
    return personagem
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- USERSERIAL
-----------------------------------------------------------------------------------------------------------------------------------------
function vRP.userSerial(number)
	local rows = vRP.Query("characters/getSerial",{ serial = number })
	return rows[1] or false
end
-----------------------------------------------------------------------------------------------------------------------------------------
-- GENERATESERIAL
-----------------------------------------------------------------------------------------------------------------------------------------
function vRP.generateSerial()
	local Passport = nil
	local serial = ""

	repeat
		serial = vRP.GenerateString("LLLDDD")
		Passport = vRP.userSerial(serial)
	until not Passport

	return serial
end

